export function filterQuestions(filters: {
  company?: string
  category?: string
  discipline?: string
  difficulty?: string
  search?: string
}): Question[] {
  let filtered = [...questionBank]

  if (filters.company) {
    filtered = filtered.filter((q) => q.company.includes(filters.company!))
  }
  if (filters.category) {
    filtered = filtered.filter((q) => q.category === filters.category)
  }
  if (filters.discipline) {
    filtered = filtered.filter((q) => q.discipline === filters.discipline)
  }
  if (filters.difficulty) {
    filtered = filtered.filter((q) => q.difficulty === filters.difficulty)
  }
  if (filters.search) {
    const term = filters.search.toLowerCase()
    filtered = filtered.filter(
      (q) =>
        q.title.toLowerCase().includes(term) ||
        q.description.toLowerCase().includes(term) ||
        q.tags.some((t) => t.toLowerCase().includes(term))
    )
  }

  return filtered.sort((a, b) => b.frequency - a.frequency)
}
